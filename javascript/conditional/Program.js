let position = 4;

// if else ------------------------------------------------------------

console.log("==== IF ELSE ===============");

if (position == 1) {

    console.log("Position: 1");

} else if (position == 2) {

    console.log("Position: 2");

} else if (position == 3) {

    console.log("Position: 3");

} else {

    console.log("Position: invalid");

}

// switch -------------------------------------------------------------

console.log("==== Switch ================");

switch (position) {

    case 1:
        console.log("Position: 1");
        break;
    case 2:
        console.log("Position: 2");
        break;
    case 3:
        console.log("Position: 3");
        break;
    default:
        console.log("Position invalid");

}